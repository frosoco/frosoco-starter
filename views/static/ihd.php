<div class="panel home">
	<h3>What is In-House Draw?</h3>
	<p>It's an event where next year's sophomores choose their rooms.</p>

	<h3>Who should go to In-House Draw?</h3>
	<p>All sophomores preassigned to FroSoCo for 2015-2016 should attend In-House Draw or send a proxy to choose a room for next year.
		If you are not represented, you will be assigned the leftover room.</p>

	<h3>What forms do I need?</h3>
	<p>Fill out the <a href="/assets/documents/IHD_Form15-16.pdf">2015-2016 In-House Draw form</a>.
	If you have a proxy representing you, make sure to fill out the portion of the form for proxies.</p>

	<h3>When and where is In-House Draw?</h3>
	<p><b>June 3, 8:30pm, in Ricker.</b> Don't be late, or we might skip over you!</p>

	<h3>Will there be food? Or other fun activities?</h3>
	<p>Yes! We will also be taking a picture of all returning sophomores before you can pick your room for the FroSoCo directory. </p>

	<h3>What rooms are available next year?</h3>
	<p>The list of rooms is available for <a href="/assets/documents/IHD_RoomAssignments15-16.pdf">download here</a>. See the sidebar on the right for floor plans of Adams and Schiff.</p>

	<h3>What are the rules for In-House Draw?</h3>
	<p><a href="https://studentaffairs.stanford.edu/sites/default/files/resed/files/2014-15%20In-House%20Draw%20Overview.pdf">A document of official rules is linked here.</a>
	ResEd hasn't updated the rules since last year, but they are still the same. Note that the section about Gender Neutral housing does not apply to FroSoCo.</p>

 	<h3>I need a roommate! What should I do?</h3>
	<p>Attend the mixer at <b>8:30 on April 23 in Ricker</b> and see if you can't nab one.</p>

