	<div class="section faq">
		<h3>Why the Heron?</h3>
		<img class="left noshadow" src="/assets/images/shield.png">
		<p>
			The heron on the shield was chosen by a small group of FroSoCo staff, students, and alumni (working with a professional designer, Corwin Stone of The Milk Farm design firm) charged with the task of developing a logo for FroSoCo. We thought that the general idea of a heraldic shield would be cool and amusing. Herons occasionally visit the berm in front of FroSoCo to hunt the rabbits there. They are beautiful and graceful birds with a noble bearing. The heron has interesting and varied symbolism in many different cultures often clustering around a combination of notions of beauty and elegance in balance with strength, power, independence and self-reliance - just the kind of balanced excellence we strive to exemplify at FroSoCo.
		</p>
	</div>

	<div class="section faq">
		<h3>What's the difference between SLE, ITALIC, and FroSoCo?</h3>
		<p>
			Immersion in the Arts: Living in Culture (ITALIC) and Structured Liberal Education (SLE) involves taking a year-long series of courses based in their respective dorms. 
			FroSoCo differs in that it has no course requirements. However, like SLE and ITALIC, we do attract intellectually intense students looking for an environment where their passions will be shared by other students. 
			Our location close to the Science and Engineering Quad means that we tend to have a large number of students interested in Science and Engineering.
		</p>
	</div>

	<div class="section faq">
		<h3>How do I get into FroSoCo?</h3>
		<p>
			If you are a newly admitted freshman, you will receive instructions over the summer on how to apply - you will need to rank FroSoCo and fill out a short essay.
		</p>
		<p>
			If you are a rising sophomore, you can either pre-assign tier 3 or draw into FroSoCo. If you are already living in FroSoCo, congratulations, you are guaranteed a spot!
			Otherwise, you will need a current FroSoCo resident to sponsor you in your pre-assignment. If you decide to draw, FroSoCo is traditionally a tier 3 dorm. Not bad for a 2-room double. 
		</p>
		<p>
			If you are a rising junior or senior, you can staff! We have CA (RA), PHE, RCC, RMC (Residential Media Consultant), and OCT (Oral Communication Tutor) positions. See the <a href="/home/staff">staff page</a> to learn more.
		</p>
	</div>

