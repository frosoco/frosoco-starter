<div class="page-header">
	<h1>Academic Resources</h1>
</div>  


<div class="panel">
	<p>
		Arik Lifschitz, FroSoCo's Academic Director (AD), is a tremendous resource and a gateway to all sorts of opportunities and resources across the entire university. 
		The AD can help you figure out what questions to ask to whom, how to navigate the system, and so forth.
	</p>

	<p>
		The <a href="//undergrad.stanford.edu">Undergrad site</a> also has helpful academic information.
	</p>

	<p>
		Every department has a Student Services Officer or Student Services Administrator, etc., who's job it is to introduce students to that department.
		It is totally fine to present yourself as someone interested in Topic X and see how they guide you in preparing for the major, suggesting people to talk to, research topics, etc. 
		You don't need to be a declared major in that department to approach the SSO.
	</p>

	<p>
		Get to know your professors. Visit their office hours. Invite them to Faculty Night. Aim to connect with 1 professor each quarter.
	</p>

	<p>
		Stanford is very different from high school, and many times students find that the skills and habits that got them into Stanford need to be adjusted. 
		If a problem set isn't going how you expect, you should ask for help (go to office hours!) instead of spending days struggling on it independently. 
		The AD and professors are great, but they can't help you unless you reach out. Stanford has a ton of resources as long as you take advantage of them.
	</p>

</div>