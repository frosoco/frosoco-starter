	<h3>Alcohol</h3>
	<p>Drinking alcohol when you are under the age of 21 is illegal in the United States; however, the staff at FroSoCo are not here to police you. We are here to help you make safe and healthy decisions, so that you can have the best Stanford experience possible. That said, FroSoCo does have an alcohol policy that we expect all residents to abide by.</p>
	<ul>
		<li>No liquor or drinking games in public spaces (e.g. lounges, kitchens, hallways, bathrooms, stair wells, the computer cluster, the Tower Room, or any other public study spaces)</li>
		<li>You may not have your door closed while drinking alcohol in your room</li>
	</ul>

	<h3>Smoking</h3>
	<p>There is no smoking of any kind within 30 feet of the building <b>including the bridge.</b></p>

	<h3>Fire/Earthquake</h3>
	<p>Our Emergency Assembly Point is towards the front of Ricker Field, right by Adams. If the fire alarm goes off, please exit the buildings at the closest exit to you and meet at the EAP. Locate your RA at that point to check-in. We have a roster and need to check you off.</p>

	<h3>Lockout Policy</h3>
	<p>You are allotted <b>two</b> free lockouts for the year. For each subsequent lockout you will need to contact the housing front desk or H.A.R.T. to come unlock your door. They will charge you a fee to unlock your door.</p>
	<p>That being said, for the first two lockouts, first try to contact your roommate to come unlock your door. If they are unavailable, (very nicely) ask the CA on your hall to assist you. If the CA in your hall is not available, then you may ask another CA from your building to unlock your door for you.</p>

	<h3>Quiet Hours</h3>
	<p>Sleep is good for learning. Quiet Hours in FroSoCo are:</p>
	<ul>
		<li>Weekdays (Sun-Thu): 11:00PM</li>
		<li>Weekends (Fri-Sat): 1:00AM</li>
	</ul>

	<h3>Common Spaces</h3>
	<p>At FroSoCo, we are very lucky to have many common spaces available for us to use! We expect you all to help ensure that these spaces are welcoming  for everyone in our community.  This means that if you make any kind of mess in the lounge, kitchen, hallways, bathrooms, etc., you are responsible for cleaning it up. We have an awesome custodial staff here at FroSoCo (be sure to say hello and to introduce yourself when you see them!), and we want to make sure that we respect them. It is not their job to clean up after us.</p>
	<p>Please do not put the trash from your room in the bathroom trashcans. There are dumpsters outside of both Adams and Schiff for your trash. Do not leave waste or furniture from your room in the hallways. This is a fire hazard.</p>
	<p>Additionally, our lounges are awesome! We've got some really great equipment in them that we want you all and future FroSoCoans to enjoy, so please take care of it. Be sure to be very careful when using any of the technology or electronics in these spaces. If you don't know how to use something, ask!</p>
