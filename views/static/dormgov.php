	<h4>Year-Long Appointments (5)</h4>
	<p>5 Positions on Dorm Gov are permanent positions for the entire academic year, requiring a larger amount of training and commitment. The number of spots available for each position (and any restrictions on who can run for them) are stated in parentheses.</p>
	<strong>Treasurer (1)</strong>
	<ul>
		<li>Manages Dorm Gov funds (quarterly budget, reimbursements, etc.)</li>
		<li>Signer for the Dorm Gov account.</li>
		<li>Works closely with the staff Finance Liaison (Megan Mikhail) as well as the RPM (Mona).</li>
		<li>By application only (application form will be sent out after house meeting). Application deadline: 9/29/2013, 11:59:pm</li>
	</ul>
	<strong>Frosh Council Representatives (1 Adams, 1 Schiff, Frosh only)</strong>
	<ul>
		<li>Represent FSC on Frosh Council (weekly meetings Wednesday nights from 8:30-10pm).</li>
		<li>Plan frosh-specific events (e.g. Frosh Formal @ Arrillaga Alumni Center).</li>
	</ul>
	<strong>Frosh Service Ambassador (1, Frosh only)</strong>
	<ul>
		<li>Represent FSC at monthly Haas Center meetings.</li>
		<li>Lead the dorm in community service projects.</li>
	</ul>
	<strong>Sustainability Representative (1)</strong>
	<ul>
		<li>Represents FSC at Green Living Council.</li>
		<li>Lead projects on educating FSC about sustainability and waste reduction.</li>
	</ul>
	<h4 style="margin-top: 30px;">15-Week Term Appointments (10)</h4>
	<p>The remaining positions on Dorm Gov are term-based appointments, with each term running for 15 weeks as the planning for Winter Quarter programs can begin in Fall Quarter. At the end of the first term, existing Dorm Gov members may choose to run again for a second term.</p>
	<strong>Co-Presidents (1 Adams, 1 Schiff)</strong>
	<ul>
		<li>Lead house meetings.</li>
		<li>Facilitators for Dorm Gov meetings.</li>
		<li>Drivers for various Dorm Gov projects and initiatives.</li>
	</ul>
	<strong>Scribe (1)</strong>
	<ul>
		<li>Takes notes at Dorm Gov meetings.</li>
		<li>Takes notes at house meetings that will be distributed to the whole dorm.</li>
	</ul>
	<strong>House Representatives (3 Adams, 3 Schiff: 1 Soph, 2 frosh from each house)</strong>
	<ul>
		<li>“Frontline” people for communicating with the residents of FSC (being the “voice” for their houses and “ears” of the Dorm Gov).</li>
		<li>Take up active roles in events/projects/initiatives</li>
		<li>These “events/projects/initiatives” include (and are not limited to) organizing and conducting official birthday celebrations in the dorm, helping the dorm to celebrate the achievements of FroSoCoans for performances, competitions, publications, general awesomeness, etc.</li>
	</ul>
	<strong>IM Representative (1)</strong>
	<ul>
		<li>Works closely with Nick to champion the FroSoCo IM sports teams!</li>
		<li>Organizing the dorm for IM sports/events and keeping the dorm updated and participating.</li>
		<li>Organizes other games for the dorm (e.g. Capture the Flag, Broomball)</li>
	</ul>
